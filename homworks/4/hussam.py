# dict

h=dict({"name":"hussam","age":23})
print (h)
print (type(h))

# complex

k=complex(8.59-7j)
print(k)
print (type(k))

# bool

stive=bool(True)
print(stive)
print(type(stive))

# range

x=range(12)
print(x)
print (type(x))

#  set
o=set({"success","future","life","development"})
print(o)
print(type(o))

# list

m=list(["success","future","life","development"])
print(m)
print(type(m))
