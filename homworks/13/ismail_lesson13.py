no=int(input("Enter the first number :"))
ab=input("Enter the operator :")
no2=int(input("Enter the second number :"))
if ab=='+':
    print(no+no2)
elif ab=='-':
     print(no-no2)
elif ab=='*':
     print(no*no2)
elif ab=='/':
     print(no/no2)
elif ab=='**':
     print(no**no2)
elif ab=='//':
     print(no//no2)